    /**
     * @brief Get sine of an angle in radians using lookup table with linear interpolation (step size of 0.5 degrees = 0.00872665 radians)
     * @param angle Angle in radians
     * @return Sine of the angle
     */
    inline float sin(float angle) {
        // Normalize angle to [0, 2*PI)
        while (angle < 0) {
            angle += M_PI * 2;
        }
        while (angle >= M_PI * 2) {
            angle -= M_PI * 2;
        }

        // Convert angle to degrees and find corresponding index in LUT
        float degrees = angle * (180.0f / M_PI);
        int index = static_cast<int>(degrees / STEP);
        float fraction = (degrees - index * STEP) / STEP;

        // Get cosine values from LUT
        float cos1 = cos_lut[index];
        float cos2 = cos_lut[index + 1];

        // Linear interpolation
        return cos1 + fraction * (cos2 - cos1);
    }
